CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name TEXT NOT NULL,
    login TEXT NOT NULL UNIQUE,
    password TEXT NOT NULL,
    role TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS categories (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS manufacturers (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS suppliers (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS pickup_points (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    address TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS products (
    article TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    category_id INTEGER NOT NULL REFERENCES categories(id),
    description TEXT NOT NULL,
    manufacturer_id INTEGER NOT NULL REFERENCES manufacturers(id),
    supplier_id INTEGER NOT NULL REFERENCES suppliers(id),
    price NUMERIC(10, 2) NOT NULL CHECK(price >= 0),
    unit TEXT NOT NULL,
    stock_quantity INTEGER NOT NULL CHECK(stock_quantity >= 0),
    discount_percent INTEGER NOT NULL CHECK(discount_percent >= 0),
    image_path TEXT
);

CREATE TABLE IF NOT EXISTS orders (
    id INTEGER PRIMARY KEY,
    pickup_point_id INTEGER NOT NULL REFERENCES pickup_points(id),
    client_full_name TEXT NOT NULL,
    pickup_code INTEGER NOT NULL,
    status TEXT NOT NULL,
    order_date DATE NOT NULL,
    delivery_date DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS order_items (
    order_id INTEGER NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    product_article TEXT NOT NULL REFERENCES products(article),
    quantity INTEGER NOT NULL CHECK(quantity > 0),
    PRIMARY KEY (order_id, product_article)
);
